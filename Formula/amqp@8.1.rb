  head "https://github.com/php-amqp/php-amqp"
  license "PHP-3.01"

  bottle do
    root_url "https://dl.bintray.com/shivammathur/extensions"
    rebuild 23
    sha256 arm64_big_sur: "e03f0fc0c08b8ee42522ebd1497e908809f87e7b931394a13c61abc876567dfe"
    sha256 big_sur:       "31acae0750b04472981fd4271637546a07de80085b54cf5ba87bf61de8541ef8"
    sha256 catalina:      "b49da96ec62799a34cce4a33f12cc47934686200466cd0678862a21bdd667e51"
  end

  depends_on "rabbitmq-c"

  def install
    args = %W[
      --with-amqp=shared
      --with-librabbitmq-dir=#{Formula["rabbitmq-c"].opt_prefix}
    ]
    patch_spl_symbols
    safe_phpize
    system "./configure", "--prefix=#{prefix}", phpconfig, *args
    system "make"
    prefix.install "modules/#{module_name}.so"
    write_config_file
  end
end
