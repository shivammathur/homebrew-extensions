  sha256 "fa2ab558fc8f0928a10f35c0f566f7c4a1d32e727bd3a96579e4c28482ee9d6a"
  head "https://github.com/m6w6/ext-http.git"
  license "BSD-2-Clause"

  bottle do
    root_url "https://ghcr.io/v2/shivammathur/extensions"
    sha256 cellar: :any,                 arm64_big_sur: "2592a2db130ca902fcc48130a23fb8270dcaa406e9be03af81b0d7f857967157"
    sha256 cellar: :any,                 big_sur:       "a42ad01c764f0d278b799d428cad6a5581caeb33cbb87276e057b16faec7547a"
    sha256 cellar: :any,                 catalina:      "099d74e828c285518c0de5b7ab34e72bae372c747f6b3bba2180f4a89500647d"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "115b44672bd3947edb9a31d606bc5ceaef1f49d883ddb5ce0e5f7e7ba2c9b78a"
  end

  depends_on "brotli"
  depends_on "curl"
  depends_on "icu4c"
  depends_on "libevent"
  depends_on "libidn2"
  depends_on "shivammathur/extensions/raphf@8.2"

  def install
    args = %W[
      --with-http
      --with-http-libicu-dir=#{Formula["icu4c"].opt_prefix}
      --with-http-zlib-dir=#{MacOS.sdk_path_if_needed}/usr
    ]
    extra_includes = %W[
      -I#{Formula["shivammathur/extensions/raphf@8.2"].opt_include}/php
    ]
    ENV["EXTRA_INCLUDES"] = extra_includes * " "
    Dir.chdir "pecl_http-#{version}"
    inreplace "src/php_http_api.h", "ext/raphf", "ext/raphf@8.2"
    patch_spl_symbols
    safe_phpize
    system "./configure", "--prefix=#{prefix}", phpconfig, *args
    system "make"
    prefix.install "modules/#{extension}.so"
    write_config_file
  end
end
